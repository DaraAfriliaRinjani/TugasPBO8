    private void BtnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnHitungActionPerformed
        // TODO add your handling code here:
        try {
        int year = Integer.parseInt(BtnHasilTahun.getText()); // Get the year from the text field
        int month = jComboBox1.getSelectedIndex() + 1; // Get the selected month from ComboBox (1-12)

        itunghari calculator = new itunghari(); // Create an instance of DateCalculator
        int days = calculator.hitungHari(year, month); // Calculate days

        // Show the result
        jLabel1.setText("Jumlah harinya adalah: " + days); // Update the label with the result
    } catch (NumberFormatException e) {
        jLabel1.setText("Tahun tidak valid!"); // Handle invalid year input
    } catch (IllegalArgumentException e) {
        jLabel1.setText("Bulan tidak valid!"); // Handle invalid month selection
    }
    }//GEN-LAST:event_BtnHitungActionPerformed

