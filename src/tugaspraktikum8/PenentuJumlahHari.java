    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        // TODO add your handling code here:
       try {
        // Get the year and month
        int year = Integer.parseInt(BtnHasilTahun.getText());
        int month = jComboBox1.getSelectedIndex() + 1; // month index is 0-based
        int days = new itunghari().hitungHari(year, month); // Calculate days

        // Prepare the data to save
        String dataToSave = "Tahun: " + year + ", Bulan: " + month + ", Jumlah Hari: " + days + "\n";

        // Specify the file name and path (can be changed as needed)
        String fileName = "jumlah_hari.txt";
        
        // Use FileWriter to write data to the file
        FileWriter writer = new FileWriter(fileName, true); // true for append mode
        writer.write(dataToSave);
        writer.close(); // Always close the writer

        jLabel1.setText("Data disimpan ke " + fileName);
    } catch (IOException e) {
        jLabel1.setText("Error menyimpan data: " + e.getMessage());
    } catch (NumberFormatException e) {
        jLabel1.setText("Tahun tidak valid!");
    } catch (IllegalArgumentException e) {
        jLabel1.setText("Bulan tidak valid!");
    }
    }//GEN-LAST:event_BtnSimpanActionPerformed

